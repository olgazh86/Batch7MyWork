package com.syntax.class09;

public class HomeWorkPattern2 {

	public static void main(String[] args) {
		
		
		for (int row=1; row<=5;row++) {
			for (int col=1; col<=row;col++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
	System.out.println("*****************");	
		
		
	for (int a=5; a>=1;a--) {
		for (int b=1; b<=a;b++) {
			System.out.print(a);
		}
		System.out.println();
	}
		
		
		
		
		
		
		

	}

}
