package com.syntax.class09;

public class MorePatterns {

	public static void main(String[] args) {
		// 12345 3 times 

		for (int row =1; row<=3;row++) {
			for (int j=1; j<=5;j++) {
				System.out.print(j);
			}
			System.out.println();
		}
		
		System.out.println("*******************");
		
		
		
		for (int row=1; row<=4;row++) {
			for (int col=1; col<=5;col++) {
				System.out.print(row);
			}
			System.out.println();
		}
		
		
		
		
	}

}
